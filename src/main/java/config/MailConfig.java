        mailSender.setPort(587);

        mailSender.setUsername("truongquoctuan45d@gmail.com");
        mailSender.setPassword("xrhb urxt myld ofks"); // App Password (bắt buộc bật 2FA)

        mailSender.setDefaultEncoding("UTF-8");

        Properties props = mailSender.getJavaMailProperties();
